from __future__ import annotations

import re
import unicodedata
from config import KNOWN_BRANDS, SUBCATEGORY_KEYWORDS


# в”Җв”Җ бғӨбғҗбғЎбғҳбғЎ бғһбғҗбғ бғЎбғҳбғңбғ’бғҳ в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
def parse_price(text: str) -> float | None:
    """
    бғӣбғҳбғ¬бғқбғ“бғ”бғ‘бғЈбғҡбғҳ бғЎбғўбғ бғҳбғңбғ’бғҳбғ“бғҗбғң бғҗбғӣбғқбғҳбғҰбғ”бғ‘бғЎ бғһбғҳбғ бғ•бғ”бғҡ бғ•бғҗбғҡбғҳбғ“бғЈбғ  GEL бғӨбғҗбғЎбғЎ.
    бғӣбғ®бғҗбғ бғЎ бғЈбғӯбғ”бғ бғЎ: '24.80вӮҫ', '24,80 GEL', '24.80', 'вӮҫ 24.80'
    """
    if not text:
        return None
    cleaned = (
        text.replace("\xa0", " ")
            .replace(",", ".")
            .replace(" ", "")
            .strip()
    )
    # бғҗбғӣбғқбғ•бғҳбғҰбғ”бғ‘бғ— бғһбғҳбғ бғ•бғ”бғҡ float-бғЎ
    m = re.search(r"(\d{1,6}\.?\d{0,2})", cleaned)
    if m:
        val = float(m.group(1))
        if 0.5 < val < 9_999:   # бғЎбғҗбғңбғҳбғўбғҗбғ бғЈбғҡбғҳ бғЁбғ”бғӣбғқбғ¬бғӣбғ”бғ‘бғҗ
            return round(val, 2)
    return None


# в”Җв”Җ бғңбғқбғ бғӣбғҗбғҡбғҳбғ–бғ”бғ‘бғЈбғҡбғҳ бғ’бғҗбғЎбғҗбғҰбғ”бғ‘бғҳ бғЁбғ”бғ“бғҗбғ бғ”бғ‘бғҳбғЎбғ—бғ•бғҳбғЎ в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
_NOISE = re.compile(
    r"[\s\-_/\\.,;:!?\(\)\[\]\"'`В«В»""'']+", flags=re.UNICODE
)
_UNITS = re.compile(
    r"\b(\d+\s*(?:бғ’|бғӣбғҡ|бғҡ|бғҷбғ’|g|ml|l|kg|gr|pcs|бғӘ|pack|pk))\b",
    flags=re.IGNORECASE | re.UNICODE,
)


def normalize_key(name: str, keep_volume: bool = True) -> str:
    """
    бғһбғ бғқбғ“бғЈбғҘбғўбғҳбғЎ бғЎбғҗбғ®бғ”бғҡбғҳбғ“бғҗбғң бғҘбғӣбғңбғҳбғЎ бғЁбғ”бғ“бғҗбғ бғ”бғ‘бғҗбғ“ бғңбғқбғ бғӣбғҗбғҡбғҳбғ–бғ”бғ‘бғЈбғҡ бғ’бғҗбғЎбғҗбғҰбғ”бғ‘бғЎ.
    бғһбғ бғҳбғңбғӘбғҳбғһбғҳ: brand + numeric_volume вҶ’ бғ”бғ бғ— бғЎбғҗбғҳбғўбғ–бғ” бғҳбғ’бғҳбғ•бғ” SKU = бғ”бғ бғ—бғҳ key.
    """
    s = name.lower().strip()
    # unicode normalization
    s = unicodedata.normalize("NFKD", s)
    # бғҗбғӣбғқбғ•бғҳбғҰбғқбғ— бғҳбғңбғӨбғқ бғӨбғ бғ©бғ®бғҳбғҡбғ”бғ‘бғҳбғ“бғҗбғң (бғ®бғҗбғңбғ“бғҗбғ®бғҗбғң бғ–бғ”бғ“бғӣбғ”бғўбғҳбғҗ)
    s = re.sub(r"\(.*?\)", " ", s)
    # бғңбғқбғҳбғ–бғҳ
    s = _NOISE.sub("", s)
    # бғ“бғҗбғўбғқбғ•бғ” бғӣбғ®бғқбғҡбғқбғ“ бғҗбғЎбғқ-бғӘбғҳбғӨбғ бғ”бғ‘бғҳ
    s = re.sub(r"[^\w]", "", s, flags=re.UNICODE)
    if not keep_volume:
        s = _UNITS.sub("", s)
    return s[:30]  # бғһбғҳбғ бғ•бғ”бғҡбғҳ 30 бғЎбғҳбғӣбғ‘бғқбғҡбғқ бғЎбғҗбғҷбғӣбғҗбғ бғҳбғЎбғҳбғҗ


# в”Җв”Җ бғ‘бғ бғ”бғңбғ“бғҳбғЎ бғҗбғӣбғқбғҰбғ”бғ‘бғҗ в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
def extract_brand(name: str) -> str:
    name_l = name.lower()
    for brand in KNOWN_BRANDS:
        if brand.lower() in name_l:
            return brand.capitalize()
    # fallback вҖ” бғһбғҳбғ бғ•бғ”бғҡбғҳ бғЎбғҳбғўбғ§бғ•бғҗ
    return name.split()[0].capitalize() if name.split() else "бғЎбғ®бғ•бғҗ"


# в”Җв”Җ бғҘбғ•бғ”бғҷбғҗбғўбғ”бғ’бғқбғ бғҳбғҳбғЎ бғ’бғҗбғңбғЎбғҗбғ–бғҰбғ•бғ бғҗ в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
def classify_subcategory(name: str) -> str:
    name_l = name.lower()
    for subcat, keywords in SUBCATEGORY_KEYWORDS.items():
        for kw in keywords:
            if kw.lower() in name_l:
                return subcat
    return "бғЎбғ®бғ•бғҗ"


# в”Җв”Җ бғӨбғҗбғЎбғ“бғҗбғҷбғҡбғ”бғ‘бғҳбғЎ % бғ’бғҗбғӣбғқбғ—бғ•бғҡбғҗ в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
def calc_discount_pct(old: float | None, new: float | None) -> float | None:
    if old and new and old > new > 0:
        return round((old - new) / old * 100, 1)
    return None
